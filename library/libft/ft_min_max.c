#include "libft.h"

int	ft_min(int nbo, int nbt)
{
	if (nbo > nbt)
		return (nbt);
	return (nbo);
}

int	ft_max(int nbo, int nbt)
{
	if (nbo > nbt)
		return (nbo);
	return (nbt);
}
